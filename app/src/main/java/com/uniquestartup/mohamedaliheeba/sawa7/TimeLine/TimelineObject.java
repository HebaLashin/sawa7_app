public class TimelineObject {
    String User_email;
    String User_name;
    String Timeline_image_url;
    String Description_area;
    String City;
    String Country;

    public String getCountry() {
        return Country;
    }

    public void setCountry(String country) {
        Country = country;
    }

    public String getUser_email() {
        return User_email;
    }

    public void setUser_email(String user_email) {
        User_email = user_email;
    }

    public String getUser_name() {
        return User_name;
    }

    public void setUser_name(String user_name) {
        User_name = user_name;
    }

    public String getTimeline_image_url() {
        return Timeline_image_url;
    }

    public void setTimeline_image_url(String timeline_image_url) {
        Timeline_image_url = timeline_image_url;
    }

    public String getDescription_area() {
        return Description_area;
    }

    public void setDescription_area(String description_area) {
        Description_area = description_area;
    }

    public String getCity() {
        return City;
    }

    public void setCity(String city) {
        City = city;
    }


}
